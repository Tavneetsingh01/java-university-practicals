package university;

public class UniversityStudent{
    String rollNo = "";
    String name = "";
    int age = 0;


    public void setRoll(String rollNo) {
        this.rollNo = rollNo;
    }
    public String getRoll() {
        return rollNo;
    }
    public void setName(String name) {
        this.name = name;
    }
    public String getName() {
        return name;
    }
    public void setAge(int age) {
        this.age = age;
    }
    public int getAge() {
        return age;
    }
}
